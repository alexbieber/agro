/**
 * Copies ALL images from agro/hero-section/ into public/images/hero/
 * and writes lib/data/heroImages.ts with the list of image paths.
 *
 * Files: 1.png, 2.png, ... -> hero-1.png, hero-2.png, ...
 * Run from project root: node scripts/copy-hero-images.js
 */

const fs = require("fs");
const path = require("path");

const projectRoot = path.join(__dirname, "..");
const heroSectionFolder = path.join(projectRoot, "hero-section");
const publicHeroFolder = path.join(projectRoot, "public", "images", "hero");
const heroDataPath = path.join(projectRoot, "lib", "data", "heroImages.ts");

if (!fs.existsSync(publicHeroFolder)) {
  fs.mkdirSync(publicHeroFolder, { recursive: true });
}

if (!fs.existsSync(heroSectionFolder)) {
  console.log("hero-section folder not found. Create agro/hero-section and add images.");
  const emptyContent = `/** Auto-generated. Run 'node scripts/copy-hero-images.js' after adding images to hero-section folder. */
export const heroImagePaths: string[] = [];
`;
  fs.writeFileSync(heroDataPath, emptyContent);
  console.log("Wrote lib/data/heroImages.ts with empty list.");
  process.exit(0);
}

const files = fs
  .readdirSync(heroSectionFolder)
  .filter((f) => /\.(png|jpg|jpeg|webp)$/i.test(f))
  .sort((a, b) => {
    const numA = parseInt(a.replace(/\D/g, ""), 10) || 0;
    const numB = parseInt(b.replace(/\D/g, ""), 10) || 0;
    return numA - numB;
  });

const paths = [];
for (let i = 0; i < files.length; i++) {
  const destName = `hero-${i + 1}.png`;
  const destPath = path.join(publicHeroFolder, destName);
  fs.copyFileSync(path.join(heroSectionFolder, files[i]), destPath);
  paths.push(`/images/hero/${destName}`);
  console.log(`Copied hero-section/${files[i]} -> public/images/hero/${destName}`);
}

const tsContent = `/** Auto-generated by scripts/copy-hero-images.js. Do not edit by hand. */
export const heroImagePaths: string[] = ${JSON.stringify(paths, null, 2)};
`;
fs.writeFileSync(heroDataPath, tsContent);
console.log(`Wrote lib/data/heroImages.ts with ${paths.length} image(s).`);
console.log("Done.");